using SuperCalculator;
using System;

namespace SuperCalculator
{
    public class Calculator
    {

        public Calculator() 
        {

        }

        public int Add(int arg1, int arg2)
        {
            int result = arg1 + arg2;
            return result;
        }
        public int Substract(int arg1, int arg2)
        {
            int result = arg1 - arg2;
            return result;
        }
    }
}